public class quatro {
    

    public static void main(String[] args) {
        
        for(int i=1; i<=10; i++){
            System.out.println("Tabuada do:");
            for (int num=1; num<=10; num++) {
                System.out.println(i+ "x" +num+ "=" +(i*num));
            }
        }
    }
}
